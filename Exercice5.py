''' Exercice 5
    ¯¯¯¯¯¯¯¯¯¯
    Écrivez une fonction Python qui imprime un message à l'écran et l'entoure
    d'une bannière. La taille de la bannière doit correspondre au message.

    Résultat attendu:
        >>> print_banner("420-3A5-EM Automatisation de tâches")
        #########################################
        ## 420-3A5-EM Automatisation de tâches ##
        #########################################
'''

def print_banner(message):
    print("#" * (len(message) + 6))
    print(f"## {message} ##")
    print("#" * (len(message) + 6))
