import random
import datetime
def lancer_dé(faces = 6):
    valeur = random.randint(1,faces)
    open('lancers.txt', 'a').write(
        f"{str(datetime.datetime.now())} : Un dé à {faces} faces a été lancé, et un {valeur} a été obtenu.\n")
    return valeur
